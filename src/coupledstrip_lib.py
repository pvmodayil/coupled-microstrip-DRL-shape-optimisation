def is_monotone(g: NDArray[np.float64], decreasing: bool) -> bool:
    dx: NDArray[np.float64] = np.diff(g)
    if decreasing:
        return bool(np.all(dx < 0))
    return bool(np.all(dx > 0)) 
    

